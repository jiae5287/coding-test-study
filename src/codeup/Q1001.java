package codeup;

public class Q1001 {

  public String hello() {
    return "Hello";
  }

  public String helloWorld() {
    return "Hello World";
  }

  public String helloAndWorld() {
    return "Hello\nWorld";
  }

  public String helloSingleQuotationMarks() {
    return "\'Hello\'";
  }

  public String helloDoubleQuotationMarks() {
    return "\"Hello\"";
  }

  public String specialAlphabet() {
    return "\"!@#$%^&*()\"";
  }

  public String directoryAddress() {
    return "\"C:\\Download\\hello.cpp\"";
  }

  public String unicode() {
    return "\u250C\u252C\u2510\n\u251C\u253C\u2524\n\u2514\u2534\u2518";
  }
}
